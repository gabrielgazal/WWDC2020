 But how exactly do we distinguish those amazing sounds?

 Duration, Timbre, Intensity and Pitch are some of the characteristics a sound has. And once sound is a vibration it is possible to see those characteristics in its waves.

* **Duration** is whether the sound is long or short;
* **Timbre** is the shape of the sound wave;
* **Intensity** is whether the sound is strong or weak and
* **Pitch** is wheather the sound frequency is high or low and this is what determines the **sound note**.
 
 You can see a schematic of the characteristics down below:
 
 ![Sound wave plus particles](caracs.png)


